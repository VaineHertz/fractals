#include "libft.h"

int	ft_intlen(int n)
{
	int len;

	if (n == -214783648)
		return (11);
	len = 0;
	if (n == 0)
		return (1);
	if (n < 0)
	{
		n = -n;
		len++;
	}
	while (n /= 10)
		len++;
	len++;
	return (len);
}
